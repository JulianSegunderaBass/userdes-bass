	<div class = "content">
		
		<p>Creating your own persona is a fun and intuitive way to shed more light on the purpose of your proposal. It gives your viewers a little taste of what your product or service is offering via characterizations of your population target.</p>

		<p>Generally speaking, personas are semi-fictional characters based on the traits or environment of your target population. This allows you to fine tune your presentation and gear it more towards your project’s goal. They are visual representations of how your proposal will be used, spread, or managed.</p>

		<p>In our group’s case, we’ve created three characters, each representing a target group for the reservation system. Three groups have been established, the Gaming Genesis members of APC, those working in the ITRO and casual gamer students.</p>

	</div>

	<div class = "content2">
		
		<h1 align = "center">Gaming Genesis Members</h1>

		<p>Gaming Genesis is a small group in APC for those with a strong passion for games. Aside from spending time gaming casually, they also take it to the next level by facilitating events that promote the benefits of gaming, while also giving new students a taste of how fun APC can be.</p>

		<p>They are a target for the system because they also work as moderators of the Game Laboratory. They help newcomers find their place by signing in upon entry, while also taking careful note of the equipment and games in the lab. The system would help them survey the interactions between visitors and the equipment, while also ensuring that the room doesn’t get too crowded, or rowdy.</p>

	</div>

	<div class = "content3">
		
		<h1 align = "center">The ITRO</h1>

		<p>Aside from Gaming Genesis, the ITRO also plays an important role in the Game Laboratory. While the moderators watch over the interactions among members and equipment, ITRO officers also watch over peripheral equipment in the lab, ranging from TVs and Laptops to the internet connection that may soon be established. </p>

		<p>They would benefit most from the equipment liability aspect of the system, as they can now see who is using what in the room in the case of (god forbid) the loss and/or damage of property. In a general sense, it also allows them to survey the activities more closely by looking at previously-set schedules and making arrangements for professors when needed.</p>

	</div>

	<div class = "content4">
		
		<h1 align = "center">Casual APC Gamers</h1>

		<p>As stated numerous times before, the Gaming Laboratory can get quite crowded, so it’s not uncommon to see groups of students waiting for a turn at the Xbox or PS4. Being an avid gaming community as a whole, many students in APC enjoy group gaming as a pastime, regardless of course or background. They would want to use their free time efficiently to enjoy a nice session with friends without any worries regarding schedules. </p>

		<p>The system would help these people reserve schedules in advance to make more accurate plans for a better experience in the room. This could also indirectly affect their performance with other classes or activities because it gives a more solid schedule for them to follow. They can easily find the time they need, before they actually need it.</p>

	</div>

	<div class = "content5">
		
		<img src="Dwayne the student.PNG" align = "left" id = "Dwayne">
		<h1 align = "left">Meet Dwayne</h1>


		<p>Dwayne is a student here in Asia Pacific College. He spends lots of his time working on his projects in his engineering class, and for the past few days, he’s been feeling rather loaded with work and exams. Now that exams are over, he wants to spend some quality time with his friends after classes. </p>

		<p>He then remembers his friend telling him about the Gaming Laboratory on the 5th floor, and that they should pay a visit one day. He checks his schedule and sees that he’s free to visit with his group later in the afternoon, but is unsure if the lab is available.</p>

		<p>Dwayne asks one of his professors, Sir Isaac, about the lab’s hours, to which he is told of the new web application that allows users to set schedules and activities. He finds a slot that fits his schedule, and arrives with his friends at the appointed time to see some others in the room. They find their reserved place and begin gaming.</p>

	</div>

	<div class = "content6">

		<img src="Isaac the moderator.PNG" align = "right" id = "Isaac">		
		<h1 align = "right">Meet Isaac</h1>


		<p>Sir Isaac is a Game Development Professor here at Asia Pacific College who also happens to be one of the founding members of Gaming Genesis. Aside from spending time gaming with his students, and moderating the activities in the room, he also uses the area for some of his classes when needed.</p>

		<p>On one particular day, he decides it would be best to deliver his lecture in the lab, but wants to do so without interfering with the time of the students. Seeing as how the reservation system prioritizes class lectures, he finds a free time slot that fits his class schedule and sets it. </p>

		<p>One of his irregular students, Dwayne, sees the lecture-based reservation while browsing the website for a schedule, and recognizes that the room will be reserved entirely for that period. He accepts the case and is thankful that he was informed well ahead of time to choose another schedule.</p>

	</div>

	<div class = "content7">
		
		
		<img src="Mara from ITRO.PNG" align = "left" id = "Mara">
		<h1 align = "left">Meet Mara</h1>


		<p>Miss Mara is an employee here at Asia Pacific College as part of the ITRO. Aside from managing the websites and other systems that keep the school running, she is also partly responsible for watching over the borrowed equipment and peripherals of the school.</p>

		<p>Mara happens to also be one of the early developers of the Game laboratory Reservation System, and regularly uses it to monitor equipment use.</p>

		<p>On one of her checks over the room, she discovers that one of the Xbox controllers has been lightly damaged, and is in need of repairs. She then checks the system to see the latest reservation involving the Xbox, and identifies it as the group scheduled by Dwayne. </p>

		<p>She informs Isaac, who is one of the head moderators of the room. Isaac, who is a professor, recognizes Dwayne and approaches him regarding the matter. He then finds out that Dwayne had mistakenly dropped the controller when heading out, but didn’t think the damage too severe. Seeing the case, Dwayne makes the appropriate payments for repairs.</p>

